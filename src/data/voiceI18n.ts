const MAPS: Record<Exclude<Lang, 'ko'>, Record<string, string>> = { en: EN, ja: JA }

// 현재 언어의 대사 텍스트(없으면 한국어 폴백)
export function voiceTextFor(id: string, lang: Lang, koText: string | undefined): string | undefined {
  if (lang === 'ko') return koText
  return MAPS[lang][id] ?? koText
}

